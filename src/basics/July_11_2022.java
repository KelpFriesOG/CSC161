		/* One way conditional statement */
		
		// Format: if(boolean expression){ some code }
		// If the boolean expression evaluates to true,
		// the block of code is evaluated.
		
		if(name == "brian"){
			System.out.println("His name is Brain!!");
		}
		
		/* Two way conditional statement */
		
		// Format: if(boolean expression){ some code }
		// else { some other code }
		
		int num = 9001;
		
		if(num > 9000) {
			System.out.println("ITS OVER 9000!!!!");
		}
		else{
			System.out.println("Oh no, you suck ass!");
		}
		
		// In this statement, if the condition is not met
		// In other words if the boolean statement evaluates to false
		// Then, the code in the else block is executed.
		// Basically there is no way out of execution,
		// One of two paths HAVE to be CHOSEN.
		
		/* Multi-way if statements */
		
		// Format: if(boolean expression){ some code }
		// else if (boolean expression){ some other code }
		// else if (boolean expression){ some other code }
		// .
		// .
		// .
		// else{ Code which is evaluated if all previous boolean statements
		// evaluate to false }
		
